import  UIKit

public class DarkMode : UIViewController {
    override public func loadView() {
        let view = UIView()
        self.view = view
        
        view.backgroundColor = #colorLiteral(red: 0.9764705882352941, green: 0.8509803921568627, blue: 0.5490196078431373, alpha: 1.0)
        
    }
}
